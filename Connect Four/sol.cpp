string who_is_winner(vector<string> pieces_position_list)
{
  int len=0,a=-1,b=-1,c=-1,d=-1,e=-1,f=-1,g=-1,n=7,p=0,v=-1,z=-1;
  vector<vector<string>> grid(n, vector<string>(n));
  vector<int> solv;
  vector<string> seq;
  vector<pair<int, char>> freq;
  vector<string> set;
  vector<char> compress;
  vector<string> queue;
  vector<int> square;
  for(int i = 0; i < n; ++i)
    for(int j = 0; j < n; ++j)
      grid[i][j] = "xx";
  int deq=0;
  for(int j = 0; j < pieces_position_list.size(); ++j) {
    deq++;
    if(pieces_position_list[j][0] == 'A') grid[6][++a] = pieces_position_list[j][2]+to_string(deq);
    if(pieces_position_list[j][0] == 'B') grid[5][++b] = pieces_position_list[j][2]+to_string(deq);
    if(pieces_position_list[j][0] == 'C') grid[4][++c] = pieces_position_list[j][2]+to_string(deq);
    if(pieces_position_list[j][0] == 'D') grid[3][++d] = pieces_position_list[j][2]+to_string(deq);
    if(pieces_position_list[j][0] == 'E') grid[2][++e] = pieces_position_list[j][2]+to_string(deq);
    if(pieces_position_list[j][0] == 'F') grid[1][++f] = pieces_position_list[j][2]+to_string(deq);
    if(pieces_position_list[j][0] == 'G') grid[0][++g] = pieces_position_list[j][2]+to_string(deq);
  }
  for(int i = 0; i < n; ++i){
    for(int j = 0; j < n; ++j){
      if(grid[i][j][0] == 'R' || grid[i][j][0] == 'Y'){
        if(j+1<n&&j+2<n&&j+3<n&&grid[i][j][0] == grid[i][j+1][0] && grid[i][j+1][0] == grid[i][j+2][0] &&grid[i][j+2][0] == grid[i][j+3][0]){set.push_back(grid[i][j]),set.push_back(grid[i][j+1]),set.push_back(grid[i][j+2]),set.push_back(grid[i][j+3]+grid[i][j][0]);}
        if(i+1<n&&i+2<n&&i+3<n&&grid[i][j][0] == grid[i+1][j][0] && grid[i+1][j][0] == grid[i+2][j][0] && grid[i+2][j][0] == grid[i+3][j][0]){set.push_back(grid[i][j]),set.push_back(grid[i+1][j]),set.push_back(grid[i+2][j]),set.push_back(grid[i+3][j]+grid[i][j][0]);}
        if(i+1<n&&i+2<n&&i+3<n&&j+1<n&&j+2<n&&j+3<n&&grid[i][j][0] == grid[i+1][j+1][0] && grid[i+1][j+1][0] == grid[i+2][j+2][0] && grid[i+2][j+2][0] == grid[i+3][j+3][0]){set.push_back(grid[i][j]),set.push_back(grid[i+1][j+1]),set.push_back(grid[i+2][j+2]),set.push_back(grid[i+3][j+3]+grid[i][j][0]);};
        if(i+1<n&&i+2<n&&i+3<n&&j-1>=0&&j-2>=0&&j-3>=0&&grid[i][j][0] == grid[i+1][j-1][0] && grid[i+1][j-1][0] == grid[i+2][j-2][0] && grid[i+2][j-2][0] == grid[i+3][j-3][0]){set.push_back(grid[i][j]),set.push_back(grid[i+1][j-1]),set.push_back(grid[i+2][j-2]),set.push_back(grid[i+3][j-3]+grid[i][j][0]);};
      }
    }
  }
  for(;len<set.size();len+=4){++p;}
  if(p==0)return "Draw";
  for(int i = 0; i < set.size(); ++i)
    if(set[i][0] == 'R' || set[i][0] == 'Y')
      set[i].erase(set[i].begin());
  for(int i = 0; i < set.size(); ++i){
    if(set[i].find('R') != string::npos || set[i].find('Y') != string::npos){
      compress.push_back(set[i][set[i].length()-1]);
      set[i].erase(set[i].begin()+set[i].length()-1);
    }
  }
  vector<vector<int>> preprocess(p, vector<int>(4));
  for(int i = 0; i < set.size();++i) square.push_back(stoi(set[i]));
  for(int i = 0; i < square.size(); ++i) {
    z++;
    if(i % 4 == 0 || z % 4 == 0){
      v++;
      z=0;
    }
    preprocess[v][z]=square[i];
  }
  for(int i = 0; i < preprocess.size(); ++i)
    for(int j = 0; j < preprocess[i].size(); ++j)
      sort(preprocess[i].begin(), preprocess[i].end(), greater<int>());
  for(int i = 0; i < preprocess.size(); ++i)
    for(int j = 0; j < 1; ++j)
      solv.push_back(preprocess[i][j]);
  int memo = min_element(solv.begin(),solv.end()) - solv.begin();
  return p==1&&compress[0]=='R'?"Red":p==1&&compress[0]=='Y'?"Yellow":p>1&&compress[memo]=='Y'?"Yellow":p>1&&compress[memo]=='R'?"Red":"Draw";
}
